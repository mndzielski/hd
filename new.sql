create or replace PROCEDURE "LOAD_FROM_TMP" AS 
l_exst NUMBER;
BEGIN   
    --def pracy--
    FOR rec in (SELECT ID_DEF_PRACY, NAZWA_PRACY, JEDNOSTKA, NORMA_MIN_H, NORMA_MAX_H, KOSZT_JEDN_PRACY, USUNIETY, ZRODLO_DANYCH NUMER_PRZYROSTU FROM TMP_DEF_PRACY ORDER BY NUMER_PRZYROSTU)
    LOOP
        SELECT CASE WHEN EXISTS ( SELECT * FROM CHD_DEF_PRACY WHERE ID_DEF_PRACY = rec.ID_DEF_PRACY) THEN 1 ELSE 0 END  INTO l_exst FROM SYS.DUAL;
    
        IF rec.USUNIETY = 0 then
            IF l_exst = 0 then
                DBMS_OUTPUT.PUT_LINE('DO INSERTU');
                INSERT INTO CHD_DEF_PRACY(ID_DEF_PRACY,NAZWA_PRACY,JEDNOSTKA,NORMA_MIN_H,NORMA_MAX_H,KOSZT_JEDN_PRACY) 
                    VALUES(rec.ID_DEF_PRACY,rec.NAZWA_PRACY,rec.JEDNOSTKA,rec.NORMA_MIN_H,rec.NORMA_MAX_H,rec.KOSZT_JEDN_PRACY);
            ELSE 
                DBMS_OUTPUT.PUT_LINE('DO UPDATE');
                UPDATE CHD_DEF_PRACY SET JEDNOSTKA=rec.JEDNOSTKA,NORMA_MIN_H=rec.NORMA_MIN_H,NORMA_MAX_H=rec.NORMA_MAX_H,KOSZT_JEDN_PRACY=rec.KOSZT_JEDN_PRACY
                WHERE ID_DEF_PRACY=rec.ID_DEF_PRACY;
            END IF;
        ELSE
            IF l_exst = 0 then
                DBMS_OUTPUT.PUT_LINE('DO IGNOROWANIA');
            ELSE 
                DBMS_OUTPUT.PUT_LINE('DO USUNIECIA');
                
                --if nie jest ju≈º w u≈ºyciu dodaƒá obsuge bledow
                
                --DELETE FROM CHD_DEF_PRACY WHERE ID_DEF_PRACY = rec.ID_DEF_PRACY;  
            END IF;
        END IF;
    END LOOP;
    
    --pracownik--
    FOR rec in (SELECT ID_PRACOWNIKA, IMIE_PRACOWNIKA, NAZWISKO_PRACOWNIKA, PESEL_PRACOWNIKA, ADRES, USUNIETY, ZRODLO_DANYCH, NUMER_PRZYROSTU 
            FROM TMP_PRACOWNIK ORDER BY NUMER_PRZYROSTU)
    LOOP
        SELECT CASE WHEN EXISTS ( SELECT * FROM CHD_PRACOWNIK WHERE PESEL_PRACOWNIKA = rec.PESEL_PRACOWNIKA) THEN 1 ELSE 0 END  INTO l_exst FROM SYS.DUAL;
    
        IF rec.USUNIETY = 0 then
            IF l_exst = 0 then
                DBMS_OUTPUT.PUT_LINE('DO INSERTU');
                
                IF rec.ZRODLO_DANYCH = 1 then
                    INSERT INTO CHD_PRACOWNIK(ID_PRACOWNIKA,IMIE_PRACOWNIKA,NAZWISKO_PRACOWNIKA,PESEL_PRACOWNIKA) 
                        VALUES(rec.ID_PRACOWNIKA,rec.IMIE_PRACOWNIKA,rec.NAZWISKO_PRACOWNIKA,rec.PESEL_PRACOWNIKA);

                ELSIF rec.ZRODLO_DANYCH = 2 then
                    INSERT INTO CHD_PRACOWNIK(ID_PRACOWNIKA,PESEL_PRACOWNIKA,ADRES) 
                        VALUES(rec.ID_PRACOWNIKA,rec.PESEL_PRACOWNIKA,rec.ADRES);
                END IF;
                
            ELSE 
                DBMS_OUTPUT.PUT_LINE('DO UPDATE');
                
                IF rec.ZRODLO_DANYCH = 1 then
                    UPDATE CHD_PRACOWNIK SET IMIE_PRACOWNIKA=rec.IMIE_PRACOWNIKA,NAZWISKO_PRACOWNIKA=rec.NAZWISKO_PRACOWNIKA
                        WHERE PESEL_PRACOWNIKA=rec.PESEL_PRACOWNIKA;

                ELSIF rec.ZRODLO_DANYCH = 2 then
                    UPDATE CHD_PRACOWNIK SET ADRES=rec.ADRES
                        WHERE PESEL_PRACOWNIKA=rec.PESEL_PRACOWNIKA;
                END IF;
            
            END IF;
        ELSE
            IF l_exst = 0 then
                DBMS_OUTPUT.PUT_LINE('DO IGNOROWANIA');
            ELSE 
                DBMS_OUTPUT.PUT_LINE('DO USUNIECIA');
                
            END IF;
        END IF;
    END LOOP;
    
    --material--
    FOR rec in (SELECT ID_MATERIALU,NAZWA_MATERIALU,JEDNOSTKA,KOSZT_JEDN,USUNIETY,ZRODLO_DANYCH,NUMER_PRZYROSTU 
            FROM TMP_MATERIAL ORDER BY NUMER_PRZYROSTU)
    LOOP
        SELECT CASE WHEN EXISTS ( SELECT * FROM CHD_MATERIAL WHERE NAZWA_MATERIALU = rec.NAZWA_MATERIALU) THEN 1 ELSE 0 END  INTO l_exst FROM SYS.DUAL;
    
        IF rec.USUNIETY = 0 then
            IF l_exst = 0 then
                DBMS_OUTPUT.PUT_LINE('DO INSERTU');
                
                IF rec.ZRODLO_DANYCH = 1 then
                    INSERT INTO CHD_MATERIAL(ID_MATERIALU,NAZWA_MATERIALU,JEDNOSTKA) 
                        VALUES(rec.ID_MATERIALU,rec.NAZWA_MATERIALU,rec.JEDNOSTKA);

                ELSIF rec.ZRODLO_DANYCH = 2 then
                    INSERT INTO CHD_MATERIAL(ID_MATERIALU,NAZWA_MATERIALU,KOSZT_JEDN) 
                        VALUES(rec.ID_MATERIALU,rec.NAZWA_MATERIALU,rec.KOSZT_JEDN);
                END IF;
                
            ELSE 
                DBMS_OUTPUT.PUT_LINE('DO UPDATE');
                
                IF rec.ZRODLO_DANYCH = 1 then
                    UPDATE CHD_MATERIAL SET JEDNOSTKA=rec.JEDNOSTKA
                        WHERE NAZWA_MATERIALU = rec.NAZWA_MATERIALU;

                ELSIF rec.ZRODLO_DANYCH = 2 then
                    UPDATE CHD_MATERIAL SET KOSZT_JEDN=rec.KOSZT_JEDN
                        WHERE NAZWA_MATERIALU = rec.NAZWA_MATERIALU;
                END IF;
            
            END IF;
        ELSE
            IF l_exst = 0 then
                DBMS_OUTPUT.PUT_LINE('DO IGNOROWANIA');
            ELSE 
                DBMS_OUTPUT.PUT_LINE('DO USUNIECIA');
                
            END IF;
        END IF;
    END LOOP;
    
    --def zuzycia--
    FOR rec in (SELECT ID_DEF_ZUZYCIA,NORMA_MIN_JEDN,NORMA_MAX_JEDN,CHD_ID_DEF_PRACY,NAZWA_PRACY,CHD_ID_MATERIALU,NAZWA_MATERIALU,USUNIETY,ZRODLO_DANYCH,NUMER_PRZYROSTU 
            FROM TMP_DEF_ZUZYCIA ORDER BY NUMER_PRZYROSTU)
    LOOP
        SELECT CASE WHEN EXISTS ( SELECT * FROM CHD_DEF_ZUZYCIA WHERE ID_DEF_ZUZYCIA = rec.ID_DEF_ZUZYCIA) THEN 1 ELSE 0 END  INTO l_exst FROM SYS.DUAL;
    
        IF rec.USUNIETY = 0 then
            IF l_exst = 0 then
                DBMS_OUTPUT.PUT_LINE('DO INSERTU');
                
                    INSERT INTO CHD_DEF_ZUZYCIA(ID_DEF_ZUZYCIA,NORMA_MIN_JEDN,NORMA_MAX_JEDN,CHD_ID_DEF_PRACY,CHD_ID_MATERIALU) 
                        VALUES(rec.ID_DEF_ZUZYCIA,rec.NORMA_MIN_JEDN,rec.NORMA_MAX_JEDN,rec.CHD_ID_DEF_PRACY,rec.CHD_ID_MATERIALU);
            ELSE 
                DBMS_OUTPUT.PUT_LINE('DO UPDATE');
                
                    UPDATE CHD_DEF_ZUZYCIA SET NORMA_MIN_JEDN=rec.NORMA_MIN_JEDN,NORMA_MAX_JEDN=rec.NORMA_MAX_JEDN,CHD_ID_DEF_PRACY=rec.CHD_ID_DEF_PRACY,CHD_ID_MATERIALU=rec.CHD_ID_MATERIALU
                        WHERE ID_DEF_ZUZYCIA = rec.ID_DEF_ZUZYCIA;      
            END IF;
        ELSE
            IF l_exst = 0 then
                DBMS_OUTPUT.PUT_LINE('DO IGNOROWANIA');
            ELSE 
                DBMS_OUTPUT.PUT_LINE('DO USUNIECIA');
                
            END IF;
        END IF;
    END LOOP;
    
END LOAD_FROM_TMP;

